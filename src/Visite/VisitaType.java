package Visite;

class VisitaType {
    private String id;
    private String nome;
    private String descrizione;
    private String frequenza;

    public VisitaType(String id, String nome, String descrizione, String frequenza) {
        this.id = id;
        this.nome = nome;
        this.descrizione = descrizione;
        this.frequenza = frequenza;
    }
}
